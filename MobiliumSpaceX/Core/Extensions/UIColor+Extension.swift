import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var colorBlack100: UIColor {
      return UIColor(red: 0.09, green: 0.106, blue: 0.22, alpha: 1)
    }
    @nonobjc class var colorBlack50: UIColor {
      return UIColor(red: 0.506, green: 0.523, blue: 0.643, alpha: 1)
    }
    @nonobjc class var colorBlack20: UIColor {
      return UIColor(red: 0.953, green: 0.957, blue: 0.98, alpha: 1)
    }
    @nonobjc class var colorBlack10: UIColor {
      return UIColor(red: 0.979, green: 0.981, blue: 0.999, alpha: 1)
    }
}
